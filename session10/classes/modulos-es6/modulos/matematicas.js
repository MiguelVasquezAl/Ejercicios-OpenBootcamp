export function suma(a,b) {
    return a+b
}

export function multiplica(a,b) {
    return a*b
}

export function eleva(a,b) {
    return a ** b
}

export function  factorial(a) {
    let e = 1
    for (let i = 2; i <= a; i++) {
        e *=i
    }
    return e
}

